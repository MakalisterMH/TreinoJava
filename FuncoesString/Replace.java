package FuncoesString;

public class Replace {
    public static void main(String[] args) {

        String nome = "Makalister";

        System.out.println(nome.replace("k", "c")); // Trocando o "K" pelo "C"

        System.out.println(nome.replace("Makalister", "Magaiver")); // Serve para a string completa ...

    }
}
